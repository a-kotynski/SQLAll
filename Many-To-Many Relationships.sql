/* Many-to-many Relationships

A many-to-many relationship exists between two entities when an instance of
Entity A can potentially be associated with many instances of Entity B.
In addition, a single instance of Entity B can potentially be associated 
with many instances of Entity A.

* Modeling Many-To-Many Relationships
The correct way to model a many-to-many relationship is to create an associative
table. This is sometimes reffered to as a junction table or a linking table.

*/